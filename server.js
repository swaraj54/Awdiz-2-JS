function findEvenCount(start, end) {
    var count = 0;
    for (var i = start; i <= end; i++) {
        // console.log(i)
        if (i % 2 == 0) {
            // console.log(i)
            count++;
        }
        // console.log(count, i,"count here")
    }
    return count;
}
